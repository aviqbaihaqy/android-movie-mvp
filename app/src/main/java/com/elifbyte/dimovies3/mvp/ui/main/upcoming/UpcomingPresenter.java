package com.elifbyte.dimovies3.mvp.ui.main.upcoming;

import com.androidnetworking.error.ANError;
import com.elifbyte.dimovies3.mvp.data.DataManager;
import com.elifbyte.dimovies3.mvp.data.network.model.MovieResponse;
import com.elifbyte.dimovies3.mvp.ui.base.BasePresenter;
import com.elifbyte.dimovies3.mvp.utils.rx.SchedulerProvider;

import javax.inject.Inject;

import io.reactivex.annotations.NonNull;
import io.reactivex.disposables.CompositeDisposable;
import io.reactivex.functions.Consumer;

/**
 * Created by janisharali on 25/05/17.
 */

public class UpcomingPresenter<V extends UpcomingMvpView> extends BasePresenter<V>
        implements UpcomingMvpPresenter<V> {

    @Inject
    public UpcomingPresenter(DataManager dataManager,
                             SchedulerProvider schedulerProvider,
                             CompositeDisposable compositeDisposable) {
        super(dataManager, schedulerProvider, compositeDisposable);
    }

    @Override
    public void onViewPrepared() {
        getMvpView().showLoading();
        getCompositeDisposable().add(getDataManager()
                .getUpcomingApiCall()
                .subscribeOn(getSchedulerProvider().io())
                .observeOn(getSchedulerProvider().ui())
                .subscribe(new Consumer<MovieResponse>() {
                    @Override
                    public void accept(@NonNull MovieResponse movieResponse) {
                        if (movieResponse != null && movieResponse.getResults() != null) {
                            getMvpView().updateMovie(movieResponse.getResults());
                        }
                        getMvpView().hideLoading();
                    }
                }, new Consumer<Throwable>() {
                    @Override
                    public void accept(@NonNull Throwable throwable) {
                        if (!isViewAttached()) {
                            return;
                        }

                        getMvpView().hideLoading();

                        // handle the error here
                        if (throwable instanceof ANError) {
                            ANError anError = (ANError) throwable;
                            handleApiError(anError);
                        }
                    }
                }));
    }
}
